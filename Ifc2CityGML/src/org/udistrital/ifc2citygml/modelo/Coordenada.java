package org.udistrital.ifc2citygml.modelo;

public class Coordenada {
	
	double x;
	double y;
	double z;
	
	public Coordenada(){
		x = 0;
		y = 0;
		z = 0;
	}
	
	public double getX() {
		return x;
	}
	public void setX(double x) {
		this.x = x;
	}
	public double getY() {
		return y;
	}
	public void setY(double y) {
		this.y = y;
	}
	public double getZ() {
		return z;
	}
	public void setZ(double z) {
		this.z = z;
	}

}
